/* 
Write the following expression in a java program:

V²- u² 2 245 
*/

public class MotionEquation {
    public static void main(String[] args) {
        // Given values
        int a = 2;
        int s = 245;

        // Calculate v² - u² = 2 * a * s
        int result = 2 * a * s;

        // Display the result
        System.out.println("The value of v² - u² is: " + result);
    }
}

/*
output 
The value of v² - u² is: 980
*/
